num1 = 10
num2 = 5

print(num1 + num2) #adição
print(num1 - num2) #subtração
print(num1 * num2) #multiplicação
print(num1 / num2) #divisão
print(num1 // num2) #divisão inteira
print(num1 % num2) #módulo
print(num1 ** num2) #exponenciação
print((num1 + num2) * 2) #ordem de precedência
print(num1 + num2 * 2) #ordem de precedência
print(num1 + (num2 * 2)) #ordem de precedência
print(num1 / 0) #divisão por zero dá erro
print(num1 // 0) #divisão por zero dá erro
print(num1 % 0) #divisão por zero dá erro
print(num1 ** 0) #qualquer número elevado a zero é 1
print(0 ** 0) #zero elevado a zero é 1
print(0 ** 10) #zero elevado a qualquer número é zero
print(10 ** 0.5) #raiz quadrada de 10
print(16 ** 0.5) #raiz quadrada de 16
print(27 ** (1/3)) #raiz cúbica de 27
print(81 ** (1/4)) #raiz quarta de 81
print(2 ** 10) #2 elevado a 10
print(3 ** 5) #3 elevado a 5
print(5 ** 3) #5 elevado a 3
print(10 ** 3) #10 elevado a 3
print(2 ** 0) #2 elevado a 0