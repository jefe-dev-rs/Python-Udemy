# Crie um programa que peça que o usuário insira um número.
# Se o número for mais que 10, imprima "O número é maior que 10".
# Se o número for menor ou igual a 10, imprima "O número é 10 ou menor".

numero = int(input("Insira um número: "))

if numero > 10:
    print("O número é maior que 10")
else:
    print("O número é 10 ou menor")
