# Tuplas são imutáveis
# Exemplos de uso de tuplas em Python

meses = ('janeiro', 'fevereiro', 'março', 'abril', 'maio', 'junho')
print(meses)  # Imprime a tupla completa
print(meses[0])  # Imprime o primeiro elemento da tupla
print(meses[-1])  # Imprime o último elemento da tupla
print(meses[1:4])  # Imprime uma fatia da tupla (do índice 1 ao 3)
print(len(meses))  # Imprime o tamanho da tupla
print(meses.count('março'))  # Conta quantas vezes 'março' aparece na tupla
print(meses.index('abril'))  # Imprime o índice de 'abril' na tupla


